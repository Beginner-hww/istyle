package com.istyle.pojo;

public class TbEvaluation {
    private Long evalId;
    private String evalPhoto;
    private String evalName;
    private String evalWoed;

    public Long getEvalId() {
        return evalId;
    }

    public void setEvalId(Long evalId) {
        this.evalId = evalId;
    }

    public String getEvalPhoto() {
        return evalPhoto;
    }

    public void setEvalPhoto(String evalPhoto) {
        this.evalPhoto = evalPhoto;
    }

    public String getEvalName() {
        return evalName;
    }

    public void setEvalName(String evalName) {
        this.evalName = evalName;
    }

    public String getEvalWoed() {
        return evalWoed;
    }

    public void setEvalWoed(String evalWoed) {
        this.evalWoed = evalWoed;
    }
}
